import * as THREE from "three";
import base64 from "base64-js";

import { latLonToCart } from "../utility/latLngToCartSystem";

import { username, password } from "../../info";

export default class Airports extends THREE.Group {
  constructor() {
    super();
    this.name = "airports";
    this.airports = airportsData;
    this.fetchURL =
      "https://opensky-network.org/api/airports/region?lamin=-90&lamax=90&lomin=-180&lomax=180&type=large_airport";
    this.renderAirports();
    // this.fetchAirports();
  }

  async renderAirports() {
    //plus two to lift the planes up
    const globeRadius = 100;

    // await this.fetchAirports();

    for (const port of this.airports) {
      const airport = new THREE.Mesh(
        new THREE.CircleGeometry(0.3),
        new THREE.MeshBasicMaterial({ color: 0x0000ab })
      );

      const [x, y, z] = latLonToCart(
        port.position.latitude,
        port.position.longitude,
        0,
        globeRadius
      );
      airport.translateX(x);
      airport.translateY(y);
      airport.translateZ(z);

      airport.lookAt(0, 0, 0);
      airport.rotateX(Math.PI);
      this.add(airport);
    }
  }

  async fetchAirports() {
    const credentials = `${username}:${password}`;
    const encoder = new TextEncoder();
    const data = encoder.encode(credentials);
    const encodedCredentials = base64.fromByteArray(data);

    const response = await fetch(this.fetchURL, {
      method: "GET",
      headers: {
        Authorization: `Basic ${encodedCredentials}`,
      },
    });

    const jsonData = await response.json();

    // this.airports = jsonData.states;
  }
}

